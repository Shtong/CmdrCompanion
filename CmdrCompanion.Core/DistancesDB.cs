        /// <summary>
        /// Tries to get a distance from the distances database embedded into the library
        /// </summary>
        /// <param name="star1">A <see cref="Star"/></param>
        /// <param name="star2">Another <see cref="Star"/></param>
        /// <param name="distance">A <see cref="Single"/> number that will contain the distance, if it is found</param>
        /// <returns>True if a distance was found, false otherwise</returns>
        public static bool TryGetDistance(Star star1, Star star2, out float distance)
        {
            if (star1 == null)
                throw new ArgumentNullException("star1");
            if (star2 == null)
                throw new ArgumentNullException("star2");

            int nameDiff = star1.Name.CompareTo(star2.Name);

            if (nameDiff == 0)
                throw new InvalidOperationException("You provided two stars with the same name");

            bool switchNames = nameDiff > 0;

            string hash = String.Format(switchNames ? "{1}#{0}" : "{0}#{1}", star1.Name, star2.Name);

            if (!_distancesRegistry.ContainsKey(hash))
            {
                distance = -1;
                return false;
            }

            distance = _distancesRegistry[hash];
            return true;
        }

        private static Dictionary<string, float> _distancesRegistry;
